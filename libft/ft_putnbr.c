#include "libft.h"

void	ft_putnbr(int n)
{
	long	lg;

	lg = n;
	if (n < 0)
	{
		ft_putchar('-');
		lg = -lg;
	}
	if (lg >= 10)
		ft_putnbr(lg / 10);
	ft_putchar((lg % 10) + 48);
}
